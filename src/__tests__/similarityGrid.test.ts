test('every data file parses and builds a non-empty tree', () => {
  expect(files.map(shortName)).toStrictEqual(Object.keys(EXPECTED));

  for (const file of files) {
    expect(trees.get(file)).not.toBeNull();
  }
});

test('a spectrum is perfectly similar to itself', () => {
  for (const file of files) {
    expect(similarity(file, file)).toBeCloseTo(1, 10);
  }
});

test('the similarity is symmetric', () => {
  for (const a of files) {
    for (const b of files) {
      expect(Math.abs(similarity(a, b) - similarity(b, a))).toBeLessThan(1e-12);
    }
  }
});

test('the full similarity grid matches the reference values', () => {
  const actual: Record<string, Record<string, number>> = {};
  const expected: Record<string, Record<string, unknown>> = {};
  for (const a of files) {
    const sa = shortName(a);
    actual[sa] = {};
    expected[sa] = {};
    for (const b of files) {
      const sb = shortName(b);
      actual[sa][sb] = similarity(a, b);
      expected[sa][sb] = expect.closeTo(EXPECTED[sa]?.[sb] ?? Number.NaN, 2);
    }
  }

  expect(actual).toStrictEqual(expected);
});

test('a 1 ppm global shift degrades similarity more than 5% noise', () => {
  // With sharp peaks (fwhm 0.05) a 1 ppm shift moves each peak far enough off
  // its original position to hurt similarity more than random 5% noise does.
  expect(
    similarity('01-three-peaks-clean.jdx', '02-three-peaks-noise-a.jdx'),
  ).toBeGreaterThan(
    similarity('01-three-peaks-clean.jdx', '05-three-peaks-global-shift.jdx'),
  );
});

test('similarity decreases monotonically as noise grows', () => {
  // 01 is clean; 02 adds 5% noise; 04 adds 20% noise. More noise, less similar.
  const clean = '01-three-peaks-clean.jdx';

  expect(similarity(clean, '02-three-peaks-noise-a.jdx')).toBeGreaterThan(
    similarity(clean, '04-three-peaks-noise-high.jdx'),
  );
});

test('two sub-threshold extra peaks barely change the spectrum', () => {
  // 13 is 03 plus two peaks at 5% height (3.5 and 6.5 ppm). It must stay a
  // near-duplicate of 03 and closer to it than to any differently-shaped
  // spectrum.
  const near = similarity(
    '03-three-peaks-noise-b.jdx',
    '13-three-peaks-tiny-extras.jdx',
  );

  expect(near).toBeGreaterThan(0.97);

  for (const other of files) {
    if (
      other === '03-three-peaks-noise-b.jdx' ||
      other === '13-three-peaks-tiny-extras.jdx'
    ) {
      continue;
    }

    expect(near).toBeGreaterThan(
      similarity('03-three-peaks-noise-b.jdx', other),
    );
  }
});

test('the point count is a nuisance factor the algorithm is sensitive to', () => {
  // 11 and 12 are the SAME spectrum (identical peaks and noise); 12 just keeps
  // every 5th point of 11. An ideal similarity would be ~1, but dropping 4/5 of
  // the points lowers it below the similarity of two independent noise draws of
  // the same peaks (02 vs 03). This documents a real weakness, not a target.
  const sameSpectrumFewerPoints = similarity(
    '11-three-peaks-1to9-dense.jdx',
    '12-three-peaks-1to9-sparse.jdx',
  );
  const differentNoiseSamePoints = similarity(
    '02-three-peaks-noise-a.jdx',
    '03-three-peaks-noise-b.jdx',
  );

  expect(sameSpectrumFewerPoints).toBeLessThan(differentNoiseSamePoints);
});

test('an uncorrected baseline hurts similarity more than moderate noise', () => {
  // 16 (linear) and 17 (parabolic) add a baseline to the clean 2/5/8 peaks. The
  // extra off-peak mass reshapes the tree, so both drop below the 5% noise case:
  // baseline correction matters as a preprocessing step.
  const clean = '01-three-peaks-clean.jdx';
  const withNoise = similarity(clean, '02-three-peaks-noise-a.jdx');

  expect(similarity(clean, '16-three-peaks-linear-baseline.jdx')).toBeLessThan(
    withNoise,
  );
  expect(
    similarity(clean, '17-three-peaks-parabolic-baseline.jdx'),
  ).toBeLessThan(withNoise);
});

test('similarity to clean is not monotonic in noise level at low noise', () => {
  // Counter-intuitively, 1% noise (18) is NOT more similar to clean than 5%
  // noise (02): low-amplitude noise still spawns seed-dependent spurious nodes,
  // so the score is dominated by the noise realization, not its amplitude.
  const clean = '01-three-peaks-clean.jdx';

  expect(similarity(clean, '18-three-peaks-noise-1pct-a.jdx')).toBeLessThan(
    similarity(clean, '02-three-peaks-noise-a.jdx'),
  );
});
